names = (
    "Andres Rodriguez",
    "Kaustubh Wankhede",
    "Joe John"
)
# While loop
f = 0
while f < 3:
    print(names[f])
    f += 1

# for loop
for name in names:
    print(name)

# for loop and a range function
for i in range(3):
    print(names[i])



# Output :
"""
Andres Rodriguez
Kaustubh Wankhede
Joe John
"""
